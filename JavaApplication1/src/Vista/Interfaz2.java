    private void botonCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCargarActionPerformed
        // TODO add your handling code here:
         try{ 
        String cadena = this.txtSalario.getText();
        String cadena2 = this.txtHora_Extra_Diurna.getText();
        String cadena3 = this.txtHora_Extra_Nocturna.getText();
         
        
        
       
        this.salario =new Calcular(cadena);
        this.horaExtraDiurna =new Calcular(cadena2);
        this.horaExtraNocturna =new Calcular(cadena3);
        this.txtImpresion.setText("Datos cargados");
        }catch(java.lang.NumberFormatException e){
            this.txtImpresion.setText("el valor ingresado no es un numero"); 
        }
    }//GEN-LAST:event_botonCargarActionPerformed

    
    
    
    
    private void botonSalarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSalarioActionPerformed
        // TODO add your handling code here
        Calcular calcular = new Calcular();
        salario.Deducciones();
        horaExtraDiurna.calcularHorasExtraDiurna();
        horaExtraNocturna.calcularHorasExtraNocturna();
        sueldo = calcular.calcularSueldo(salario,horaExtraDiurna,horaExtraNocturna);
        this.txtImpresion.setText("Su salario es: "+sueldo.toString());
    }//GEN-LAST:event_botonSalarioActionPerformed

